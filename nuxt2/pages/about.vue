      <h3>Ощутили землетрясение?</h3>

      <p>
        В сервисе реализован сбор откликов от населения через интернет.
        Обратную связь от населения сервис получает с помощью
        опросного листа «Ощутили землетрясение?».
        Ответив на несколько вопросов анкеты, пользователи помогают
        уточнить интенсивность колебаний в области воздействия
        землетрясения. Автоматическая оценка макросейсмической
        интенсивности производится в тех населенных пунктах,
        из которых поступили отклики. Обработанная информация
        доступна пользователям сервиса без ограничений.
        <br/>
        Помимо измерения макросейсмических эффектов через
        отклики от населения, интенсивность в ближайших
        населенных пунктах моделируется согласно эмпирическому
        уравнению макросейсмического поля.
      </p>

