      <p><strong>Средневзвешенный анализ</strong></p>
      <p>
        Систематическая недооценка реального уровня сейсмических
        колебаний может приводить к искаженной картине ситуационной
        обстановки и недооценке реальных последствий. Это в свою
        очередь влияет на безопасность людей, которые оказались в
        зоне интенсивных сейсмических воздействий, способных вызвать
        повреждения объектов городской инфраструктуры.
      </p>
      <p>
        <a
          href="https://fips.ru/publication-web/publications/document?type=doc&tab=PrEVM&id=F00690DE-5FEE-4F0D-9131-C12EF2EC1915"
          target="_blank"
        >Уникальная методика</a>
        моделирования поля средневзвешенных параметров сейсмических
        воздействий, разработанная и реализованная в рамках сервиса,
        производит корректировку модельных значений сейсмических ускорений
        в относительно большом радиусе от места измерения ускорения или
        макросейсмической интенсивности.
      </p>
      <p>
        Таким образом, при наступлении сейсмического события в
        экспресс-режиме генерируется карта сотрясаемости в физически
        измеряемых единицах. Это является важным этапом, предваряющим
        оценку риска повреждения объектов городской инфраструктуры.
      </p>

