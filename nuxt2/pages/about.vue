      <p><strong>Методика обработки откликов</strong></p>
      <p>
        Для оперативной оценки сейсмической интенсивности
        по откликам от населения используется опросный лист
        в соответствии с методикой DYFI [Wald et al., 1999].
        Ответы респондентов в каждом населенном пункте
        приводятся к взвешенной сумме CWS с учетом различных
        индикаторов сейсмических колебаний: ощущение людьми,
        положение предметов и мебели в пространстве, характер
        повреждения зданий. Полученные оценки приводятся к
        интенсивности ШСИ-2017 с округлением до десятых.
      </p>
      <p>
        При заполнении формы респондент может выбрать из
        списка землетрясение, с которым, по его мнению,
        связаны ощутимые колебания. С целью обработки ранних
        откликов, когда еще недоступна информация о
        произошедшем землетрясении, разработан алгоритм
        автоматического поиска и привязки неассоциированных
        откликов к конкретному сейсмическому событию после
        появления о нем информации. При обработке откликов
        учитывается геолокация респондента и производится
        привязка полученных ответов к конкретному населенному пункту.
      </p>
