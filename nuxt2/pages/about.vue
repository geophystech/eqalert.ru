      <h3>Построение карт сотрясаемости</h3>

      <p>
        Моделирование поля сейсмических воздействий и генерация
        карт сотрясаемости запускаются после того, как в базе данных
        сервиса появляется новое событие. Карты строятся на основе
        смоделированных количественных характеристик движения грунта в
        каждой точке пространства по эмпирическому уравнению затухания
        сейсмических воздействий и с учетом геопространственных данных.
        Закон затухания определяет характер затухания сейсмических
        воздействий с расстоянием в зависимости от магнитуды
        сейсмического события. Уровень сейсмических воздействий
        зависит также от грунтовых условий в пункте наблюдения,
        слоистости среды и других количественных параметров
        геофизической среды и очага землетрясения.
      </p>
      <p>
        В качестве меры сейсмических воздействий используются
        пиковое ускорение грунта и сейсмическая интенсивность.
      </p>
      <p>
        Расчет поля сейсмических воздействий выполняется на сетке с
        шагом около 500 м. При запуске расчетного модуля загружается
        сетка, заданная в конфигурационном файле. В заданных
        точках выполняется расчет параметров сейсмических
        воздействий по заданным уравнениям затухания.
        Пиковое усокрение грунта рассчитывается в %g и для
        удобства интерпретации приводится к баллу ШСИ-2017 согласно
        <a
          href="https://eqalert.ru/static/pdf/gost-57546-2017.pdf"
          target="_blank"
        >ГОСТ Р 57546-2017</a>.
        Поле сейсмической интенсивности моделируется в баллах шкалы MSK-64.
      </p>
      <p>
        Отрисовка карт сотрясаемости выполняется в виде изолиний,
        соответствующих баллам сейсмической интенсивности.
        В качестве подложек используются Яндекс.Карты и Google Maps.
      </p>

